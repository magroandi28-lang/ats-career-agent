  (10436, 'szakmai tapasztalat')
) as v(id, f)
where k.id = v.id;

create or replace view public.v_szakma_fogalmak as
select
    sz.id  as szakma_id,
    sz.nev as szakma,
    coalesce(k.fogalom, k.kanonikus, k.nev) as fogalom,
    count(distinct h.id) as elofordulas,
    round(100.0 * count(distinct h.id) / nullif(
        (select count(*) from public.hirdetesek h2 where h2.szakma_id = sz.id), 0), 1
    ) as hirdetesek_szazaleka
from public.hirdetesek h
join public.szakmak sz          on sz.id = h.szakma_id
join public.hirdetes_keszseg hk on hk.hirdetes_id = h.id
join public.keszsegek k         on k.id = hk.keszseg_id
group by sz.id, sz.nev, coalesce(k.fogalom, k.kanonikus, k.nev)
order by szakma, elofordulas desc;

alter view public.v_szakma_fogalmak set (security_invoker = on);
